# Directories inside the template that must not be searched when looking for files to overwrite.
# Keeps the same spirit as IGNORE_PATTERNS above.
SCAN_IGNORE_DIRS = {".git", "__pycache__", ".venv", ".vscode", "output", "docs"}

# Files and folders from the zip that must never be placed into the temp clone,
