# 2) Adjust the SUBMISSIONS_DIR and RESULT_FILE paths in the CONFIGURATION section below if needed.
# 3) Run this script. It will create a CSV file with the evaluation results for each submission.
# 
# HOW IT WORKS:
# This script is designed to evaluate one or more task3 submissions packaged as zip files. Each
# zip file can contain one or more top-level folders with '_task3' in their name, each representing
# an independent submission to evaluate. For each such submission folder, the script creates a
# temporary copy of the current project template, extracts the submission files on top of it according
# to specific rules, and runs the submission_test.py script in an isolated environment. The results
# are collected from a yaml file generated by submission_test.py and saved into a CSV file for easy comparison.

# INFO: 
# For single submission testing, put only the zip file of that submission in the folder and run this script, then check the csv for the results. 
# Doesnt use generated code from submission, only the model files, config_submission.yaml, feature_handler.py, inference_handler.py and custom code (placed in root) from the zip file.
# Important: script is dependent on the folder structure being correct, or else the evaluation of the submission will fail. The error will be saved in the csv file as well.
# Important: needs keras/pth and tflite model, so no possibility to only submit one of them, otherwise the evaluation will fail and the error will be saved in the csv file as well.

